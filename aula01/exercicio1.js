//Leia 2 valores A e B, que correspondem a 2 notas de um aluno.
//A seguir calcule e informe a média do aluno, sabendo que a nota A tem peso 4 e a nota B tem peso 6.

const notaA = 4; //atribuindo  variável nota o valor 4
const notaB = 6; //atribuindo  variável nota o valor 6

media = (notaA + notaB) / 2; //calculo da media
console.log(`A média do aluno é ${media}`); //mostrar o resultado
